    private void jtUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtUsersMouseClicked

        userModel.setId(0);
        userModel.setNome("");
        userModel.setLogin("");
        userModel.setFoneuser("");
        userModel.setTipo_usuario("");
        if (jtUsers.getSelectedRow() != -1) {

            // DefaultTableModel modelo = (DefaultTableModel) jtUsers.getModel();
            userModel.setId(Integer.parseInt(jtUsers.getValueAt(jtUsers.getSelectedRow(), 0).toString()));
            //System.out.println(jtUsers.getValueAt(jtUsers.getSelectedRow(), 0).toString());
            userModel.setNome(jtUsers.getValueAt(jtUsers.getSelectedRow(), 1).toString());
            //System.out.println(jtUsers.getValueAt(jtUsers.getSelectedRow(), 0).toString());
            userModel.setLogin(jtUsers.getValueAt(jtUsers.getSelectedRow(), 2).toString());
            // System.out.println(jtUsers.getValueAt(jtUsers.getSelectedRow(), 1).toString());
            userModel.setFoneuser(jtUsers.getValueAt(jtUsers.getSelectedRow(), 3).toString());
            // System.out.println(jtUsers.getValueAt(jtUsers.getSelectedRow(), 2).toString());
            int tipo;
            if ("Usuario".equals(jtUsers.getValueAt(jtUsers.getSelectedRow(), 4).toString())) {
                tipo = 0;

            } else {
                tipo = 1;
            }
            userModel.setTipo_usuario(Integer.toString(tipo));
            //System.out.println(jtUsers.getValueAt(jtUsers.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_jtUsersMouseClicked

