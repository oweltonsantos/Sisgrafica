    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jtUsers.getModel();
        modelo.setNumRows(0);
        ControllerUsuario userContr = new ControllerUsuario();

        DefaultTableCellRenderer tabela = new DefaultTableCellRenderer();
        tabela.setHorizontalAlignment(SwingConstants.LEFT);
        jtUsers.getColumnModel().getColumn(0).setCellRenderer(tabela);

        for (ModelUsuario user : userContr.read()) {

            modelo.addRow(new Object[]{
                user.getId(),
                user.getNome(),
                user.getLogin(),
                user.getFoneuser(),
                user.getTipo_usuario(),});

        }

    }

    public void readJTableQuery(String usuario) {

        DefaultTableModel modelo = (DefaultTableModel) jtUsers.getModel();
        modelo.setNumRows(0);
        ControllerUsuario userContr = new ControllerUsuario();

        DefaultTableCellRenderer tabela = new DefaultTableCellRenderer();
        tabela.setHorizontalAlignment(SwingConstants.LEFT);
        jtUsers.getColumnModel().getColumn(0).setCellRenderer(tabela);

        for (ModelUsuario user : userContr.readQuery(usuario)) {

            modelo.addRow(new Object[]{
                user.getId(),
                user.getNome(),
                user.getLogin(),
                user.getFoneuser(),
                user.getTipo_usuario(),});

        }

    }

