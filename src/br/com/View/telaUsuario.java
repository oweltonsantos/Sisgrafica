    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jtUsers.getSelectedRow() != -1) {
            System.out.println("ID: " + jtUsers.getValueAt(jtUsers.getSelectedRow(), 0) + "Nome: " + jtUsers.getValueAt(jtUsers.getSelectedRow(), 1));
            int op = JOptionPane.showConfirmDialog(this, "Deseja Mesmo Excluir o Usuário de ID = " + jtUsers.getValueAt(jtUsers.getSelectedRow(), 0)
                    + " " + "Nome: " + jtUsers.getValueAt(jtUsers.getSelectedRow(), 1), "Aviso do Sistema", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {
                ModelUsuario u = new ModelUsuario();
                ControllerUsuario controller = new ControllerUsuario();

                u.setId((int) jtUsers.getValueAt(jtUsers.getSelectedRow(), 0));

                controller.delete(u);

                // txtDesc.setText("");
                // txtQtd.setText("");
                // txtPreco.setText("");
                readJTable();

            }
            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

